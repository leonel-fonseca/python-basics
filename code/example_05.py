"""
Example of Sequence types.
"""

[1, 2, 3]  # List of integers

["a", "b", "c"]  # List of characters

["a", 1, True]  # List of mixed types

("a", 1, True)  # Tuple of mixed types (immutable)

my_list = [1, 2, 3]  # Creating a list

my_list = [] # This is an empty list

my_list.append(1)  # Adding an element to the list
