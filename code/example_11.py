# Logic. Case Analysis

def classify_animal(animal):
    if animal == "cat":
        print("It's a cat")
    elif animal == "dog":
        print("It's a dog")
    elif animal == "bird":
        print("It's a bird")
    elif animal == "fish":
        print("It's a fish")
    else:
        print("I don't know that animal")

classify_animal("cat")

classify_animal("dog")

classify_animal("elephant")
